#!/bin/bash

echo "Привет! Я бот. Напишите одну из команд, чтобы я выполнил действие:"
echo "Команды:"
echo "  Дата     - вывести текущую дату"
echo "  Время    - вывести текущее время"
echo "  Привет   - поприветствовать"
echo "  Выход    - завершить работу"

while true; do
  # Считываем ввод пользователя
  read -p "Введите команду: " command
  
  case $command in
    "Дата")
      echo "Текущая дата: $(date +%Y-%m-%d)"
      ;;
    "Время")
      echo "Текущее время: $(date +%H:%M:%S)"
      ;;
    "Привет")
      echo "Привет! Как я могу вам помочь?"
      ;;
    "Выход")
      echo "До свидания!"
      break
      ;;
    *)
      echo "Неизвестная команда: $command. Попробуйте ещё раз."
      ;;
  esac
done

