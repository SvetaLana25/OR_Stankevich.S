#!/bin/bash

# Проверяем, передан ли один аргумент (длина пароля)
if [ $# -ne 1 ]; then
  echo "Использование: $0 <длина пароля>"
  exit 1
fi

# Проверяем, что длина является положительным числом
LENGTH=$1
if ! [[ "$LENGTH" =~ ^[0-9]+$ ]] || [ "$LENGTH" -le 0 ]; then
  echo "Ошибка: Длина пароля должна быть положительным числом."
  exit 1
fi

# Устанавливаем локаль
export LC_ALL=C

# Генерация пароля
PASSWORD=$(tr -dc 'A-Za-z0-9' < /dev/urandom | head -c "$LENGTH")

# Вывод пароля
echo "Сгенерированный пароль: $PASSWORD"


