#!/bin/bash

# Проверяем, переданы ли два аргумента
if [ $# -ne 2 ]; then
  echo "Использование: $0 <число1> <число2>"
  exit 1
fi

# Аргументы
NUM1=$1
NUM2=$2

# Проверяем, являются ли аргументы числами
if ! [[ "$NUM1" =~ ^-?[0-9]+$ && "$NUM2" =~ ^-?[0-9]+$ ]]; then
  echo "Ошибка: Оба аргумента должны быть числами."
  exit 1
fi

# Сравнение чисел
if [ "$NUM1" -gt "$NUM2" ]; then
  echo "Число $NUM1 больше числа $NUM2."
elif [ "$NUM1" -lt "$NUM2" ]; then
  echo "Число $NUM1 меньше числа $NUM2."
else
  echo "Числа $NUM1 и $NUM2 равны."
fi

