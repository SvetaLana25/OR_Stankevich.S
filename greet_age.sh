#!/bin/bash
# Запрашиваем имя пользователя
echo "Как тебя зовут?"
read name

# Запрашиваем возраст пользователя
echo "Сколько тебе лет?"
read age

# Проверяем, является ли возраст числом
if ! [[ "$age" =~ ^[0-9]+$ ]]; then
    echo "Ошибка: возраст должен быть числом."
    exit 1
fi

# Вычисляем возраст через год
next_age=$((age + 1))

# Выводим сообщение
echo "Привет, $name! Через год тебе будет $next_age лет."

