#!/bin/bash

# Массив команд для выполнения
commands=(
  "sleep 3 && echo 'Команда 1 завершена'"
  "sleep 2 && echo 'Команда 2 завершена'"
  "sleep 5 && echo 'Команда 3 завершена'"
)

# Запуск каждой команды в фоне
pids=() # Для хранения идентификаторов процессов (PID)
for cmd in "${commands[@]}"; do
  eval "$cmd &" # Запуск команды в фоне
  pids+=($!)    # Сохраняем PID процесса
done

# Ожидание завершения всех процессов
for pid in "${pids[@]}"; do
  wait "$pid" # Ожидание завершения процесса с указанным PID
done

echo "Все команды завершены."

