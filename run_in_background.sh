#!/bin/bash

# Проверяем, передана ли команда
if [ $# -eq 0 ]; then
  echo "Ошибка: Укажите команду для выполнения."
  echo "Пример: $0 \"sleep 10\""
  exit 1
fi

# Запускаем указанную команду в фоне
"$@" &
COMMAND_PID=$!

# Выводим PID запущенного процесса
echo "Команда '$*' запущена в фоне с PID: $COMMAND_PID"

