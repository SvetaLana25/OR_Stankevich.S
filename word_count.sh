#!/bin/bash

# Проверяем, переданы ли два аргумента
if [ $# -ne 2 ]; then
  echo "Использование: $0 <имя файла> <слово для поиска>"
  exit 1
fi

# Аргументы
FILE=$1
WORD=$2

# Проверяем, существует ли файл
if [ ! -f "$FILE" ]; then
  echo "Ошибка: Файл '$FILE' не найден."
  exit 1
fi

# Подсчёт количества вхождений слова
COUNT=$(grep -o "\b$WORD\b" "$FILE" | wc -l)

# Вывод результата
echo "Слово '$WORD' встречается в файле '$FILE' $COUNT раз(а)."

