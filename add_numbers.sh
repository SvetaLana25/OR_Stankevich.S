#!/bin/bash

# Функция add, которая принимает два аргумента и выводит их сумму
add() {
  local num1=$1
  local num2=$2

  # Проверяем, что оба аргумента являются числами
  if [[ ! "$num1" =~ ^-?[0-9]+$ || ! "$num2" =~ ^-?[0-9]+$ ]]; then
    echo "Ошибка: оба аргумента должны быть числами."
    return 1
  fi

  # Вычисляем сумму
  local sum=$((num1 + num2))
  echo "Сумма: $sum"
}
# Пример вызова функции
add "$1" "$2"
